<p>កាលពីថ្ងៃទី១៨ ខែវិច្ឆិកា ឆ្នាំ២០១៤ ក្រុមហ៊ុនអ៊ីហ្ស៊ីខមបានក្លាយជាក្រុមហ៊ុនដំបូងគេនៅលើពិភពលោក ដែលទទួលបានវិញ្ញាបនប័ត្រសុវត្ថិភាពកុមារ ៧ ចំណុចក្នុងអាជីវកម្ម (ChildSafe Business 7) ។ វិញ្ញាបនប័ត្រសុវត្ថិភាពកុមារ ៧ ចំណុចក្នុងអាជីវកម្ម ត្រូវបានបង្កើតឡើងដោយបណ្តាញសុវត្ថិភាពកុមារ (ChildSafe Network)ដែលជាកម្មវិធីគាំពារកុមារជាអន្តរជាតិបង្កើតឡើង និងគ្រប់គ្រងដោយអង្គការ Friends-International។ដើម្បីទទួលបានវិញ្ញាបនប័ត្រ ក្រុមហ៊ុនត្រូវផ្សារភ្ជាប់គោលការណ៍សុវត្ថិភាពកុមារ ៧ ចំណុចទៅក្នុងប្រតិបត្តិការរបស់ខ្លួន តាមរយៈការបណ្តុះបណ្តាលបុគ្គលិក ការកែលម្អគោលនយោបាយផ្ទៃក្នុង ការវាយតម្លៃសកម្មភាពសង្គម និងទទួលយកការប្រឹក្សាដើម្បីធានាថា ផែនការផ្សព្វផ្សាយរបស់ក្រុមហ៊ុនមានការពិចារណាដល់កុមារ។</p>
<p>លោក Paul Blanche-Horganប្រធានក្រុមហ៊ុនអ៊ីហ្ស៊ីខម មានប្រសាសន៍ថា “ក្រុមហ៊ុនអ៊ីហ្ស៊ីខមមានមោទនភាពដែលបានសហការជាដៃគូជាមួយអង្គការមិត្តសំឡាញ់ និងបានក្លាយជាក្រុមហ៊ុនដែលបំពេញបានតាមគោលការណ៍ទាំង ៧ និងបានការទទួលស្គាល់ពីកម្មវិធីសុវត្ថិភាពកុមារដំបូងគេនៅប្រទេសកម្ពុជា។ក្រោយពីបានទទួលការបណ្តុះបណ្តាលអំពីសុវត្ថិភាពកុមារជាច្រើនសប្តាហ៍ បុគ្គលិករបស់យើងទទួលបានវិញ្ញាបនប័ត្រ សម្រាប់ការខិតខំប្រឹងប្រែងរបស់ពួកគេកន្លងមក”។</p>
<h3 style="font-family: 'Bayon', cursive;">ការគាំទ្រ</h3>
<p>អ្នកនាង ខៀវ សានសាណា ពិធីការនីប្រចាំកម្មវិធីពេញចិត្ត ឬអត់? និងលោកដួង សុខគា ពិធីករក្នុងកម្មវិធីតើអ្នកឆ្លាតជាងសិស្សថ្នាក់ទី៥ឬទេ? បានបង្ហាញវត្តមាននៅក្នុងស្តង់ក្រុមហ៊ុន អ៊ីហ្ស៊ីខម នៅឯកម្មវិធីតាំងពិព័រណ៌អេឡិចត្រូនិច និងទូរគមនាគមន៍អន្តរជាតិ(ITET) នៅមជ្ឈមណ្ឌលពីពណ៌កោះពេជ្រ កាលពីព្រឹកថ្ងៃទី១៤ ខែវិច្ឆិកា ដើម្បីបង្ហាញនូវការគាំទ្ររបស់ពួកគេចំពោះកិច្ចផ្តួចផ្តើមសុវត្ថិភាពកុមារ ។</p>
<p>ជាមួយនឹងអាវយឺតគាំទ្រកម្មវិធីសុវត្ថិភាពកុមារ លោក សុខគា បានថ្លែងថា “ដូចដែលលោកអ្នកជ្រាបស្រាប់ហើយថា ខ្ញុំជាពិធីករក្នុងកម្មវិធីតើអ្នកឆ្លាតជាងសិស្សថ្នាក់ទី៥ឬទេ? ជាកម្មវិធីមួយដែលខ្ញុំត្រូវធ្វើការជាមួយកុមារ។ ដូច្នេះខ្ញុំគិតថាកិច្ចផ្តួចផ្តើម និងការប្រគល់វិញ្ញាបនប័ត្រនេះមានសារសំខាន់ខ្លាំងណាស់។ តាមរយៈការចូលរួមផ្សព្វផ្សាយនៅក្នុងកម្មវិធីនេះ ខ្ញុំសង្ឃឹមថាក្រុមហ៊ុនដទៃទៀតនឹងអនុវត្តដូចជាក្រុមហ៊ុនអ៊ីហ្ស៊ីខមដែរ។”</p>
<p>ពិធីការនី ខៀវ សានសាណា ថ្លែងឲ្យដឹងដែរថា “ក្រុមហ៊ុនជាច្រើនទៀតគួរតែសហការជាមួយអង្គការមិត្តសំឡាញ់ ដើម្បីរៀបចំកម្មវិធីបណ្តុះបណ្តាលបែបនេះ។ យើងចាំបាច់ត្រូវបង្កើតកម្មវិធីបណ្តុះបណ្តាលនេះឲ្យបានច្រើនតាមតែអាចធ្វើទៅបាន ដើម្បីធ្វើយ៉ាងណាការពារសុវត្ថិភាពកូនៗរបស់យើង និងដើម្បីឲ្យអ្នកដែលមានទំនួលខុសត្រូវយល់ដឹងពីអ្វីដែលពួកគេអាចធ្វើទៅបានក្នុងការការពារសុវត្ថិភាពកុមារ។ ហើយខ្ញុំសូមគាំទ្រយ៉ាងពេញទំហឹងចំពោះកិច្ចផ្តួចផ្តើមនេះ។”</p>


