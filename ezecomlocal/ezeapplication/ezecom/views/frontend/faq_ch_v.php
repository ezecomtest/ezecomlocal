</div>
<h1 class="faqheading q3" style="font-size: 14px;" data-toggle="collapse" data-parent="#accordion" data-target="#e3">What can I do if my IP address is blacklisted?</h1>
<div id="e3" class="panel-collapse collapse">
<p style="font-size: 14px;">You need to delist your IP from the blacklist using the following methods:</p>
<ul style="font-size: 14px;">
<li>Self-Service Removal. There are a few blacklists with a self-service removal feature that lets you take your IP address off the list without much trouble. However, you'll want to make sure you've resolved any issues before doing this. If you don't and your IP address gets listed again, it won't be easy to get it removed that next time.</li>
<li>Time-Based Removal. Most blacklists have a built-in, automatic process that removes lower-level listings (IP addresses that are light offenders) within a week or two. But if the IP address had sent spam more than once or send off a high volume of spam, the time period for being removed will be longer.</li>
</ul>
</div>
