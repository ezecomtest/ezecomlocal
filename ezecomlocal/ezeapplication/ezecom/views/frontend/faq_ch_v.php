<h1 class="faqheading q1" style="font-size: 14px;" data-toggle="collapse" data-parent="#accordion" data-target="#e1">How do spammers affect your IP reputation?</h1>
<div id="e1" class="panel-collapse collapse">
<p style="font-size: 14px;">Spammers steal user accounts and compromise mail servers exploiting security gaps. Once an account or server has been compromised, large amounts of spam can be sent – all without your knowledge. When email receivers see spam coming from your account or server, they may block you temporarily.</p>
</div>
<h1 class="faqheading q2" style="font-size: 14px;" data-toggle="collapse" data-parent="#accordion" data-target="#e2">What can I do if my IP address is blacklisted?</h1>
<div id="e2" class="panel-collapse collapse">
<p style="font-size: 14px;">You can check your IP against many search tools available for free online. For example:</p>
<p style="font-size: 14px;"><a href="http://mxtoolbox.com/" target="_blank">http://mxtoolbox.com</a></p>
<p style="font-size: 14px;"><a href="https://www.spamhaus.org/lookup/" target="_blank">https://www.spamhaus.org/lookup/</a></p>
