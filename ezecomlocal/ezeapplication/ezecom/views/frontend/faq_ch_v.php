<h1 class="faqheading q6" style="font-size: 14px;" data-toggle="collapse" data-parent="#accordion" data-target="#e6">How can I protect my network from spam?</h1>
<div id="e6" class="panel-collapse collapse">
<p style="font-size: 14px;">There are several things you may need to do to protect your LAN from spam:</p>
<ul style="font-size: 14px;">
<li>Scan all computers on your network for viruses</li>
<li>See if there are any known and needed "patches" (updates and fixes) for your operating system</li>
<li>Configure routers more securely</li>
<li>Establish and enforce stronger passwords</li>
<li>Configure mail server correctly</li>
