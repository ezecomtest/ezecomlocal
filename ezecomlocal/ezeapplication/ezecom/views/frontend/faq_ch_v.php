<h1 class="faqheading q3" style="font-size: 14px;" data-toggle="collapse" data-parent="#accordion" data-target="#d3">What are best practices for email?</h1>
<div id="d3" class="panel-collapse collapse">
<p style="font-size: 14px;">Here are recommendations to ensure delivery of your business email communication. Due to increasing problems with spam and malicious email developed by hackers attempting to exploit access to computer systems, today’s email server system are equipped with sophisticated tools and rules (sender policies) to counter malicious attacks. Occasionally an email cannot be delivered to a recipient because the sender server does not meet the receiver sender policy framework (SPF). Here are the do’s and don’t.</p>
<p style="font-size: 14px;"><strong>Do:</strong></p>
<ul style="font-size: 14px;">
<li>Ensure your email delivery server meets standard SPF (see <a href="http://openspf.org/" target="_blank">http://openspf.org</a>)</li>
<li>Keep the server secured by applying updates and security fixes</li>
<li>Keep your email simple and unique</li>
<li>Protect your system from virus and malware attacks</li>
<li>Use external mass-delivery services like mailchip.com if you need to push advertisements to your customers</li>
<li>Use modern and secure file sharing platforms to share large files</li>
</ul>
<p style="font-size: 14px;"><strong>Don't:</strong></p>
<ul style="font-size: 14px;">
<li>Use your business email system to send advertising emails</li>
<li>Send many recipients (more than 10) at once</li>
<li>Send large files (larger than 5Mb)</li>
<li>Include pictures in your email signature</li>
</ul>
</div>
